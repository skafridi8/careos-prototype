-- ============================================================
-- 7. risk_scores — simple weighted per-client score, refreshed by
--    refresh_risk_scores() whenever relevant data changes.
-- ============================================================
create table if not exists public.risk_scores (
  client_id text primary key references public.clients (id) on delete cascade,
  score integer not null default 0,       -- 0-100
  band text not null default 'Low' check (band in ('Low', 'Medium', 'High')),
  factors jsonb not null default '{}'::jsonb, -- breakdown shown in the UI
  updated_at timestamptz not null default now()
);

-- Weighted formula (documented so the UI can explain it):
--   open high-severity insights × 20, medium × 10, low × 5
-- + missed check-ins in the last 14 days × 8  (assigned visit-days with no check_in logged)
-- + medication-related open insights × 5 bonus (medication risk compounds)
-- capped at 100. Band: >=60 High, >=30 Medium, else Low.
create or replace function public.refresh_risk_scores()
returns void
language plpgsql
security definer set search_path = public
as $$
begin
  insert into public.risk_scores (client_id, score, band, factors, updated_at)
  select
    c.id,
    least(100, coalesce(i.high, 0) * 20 + coalesce(i.med, 0) * 10 + coalesce(i.low, 0) * 5
             + coalesce(m.missed, 0) * 8 + coalesce(i.med_issue, 0) * 5) as score,
    case
      when least(100, coalesce(i.high, 0) * 20 + coalesce(i.med, 0) * 10 + coalesce(i.low, 0) * 5
               + coalesce(m.missed, 0) * 8 + coalesce(i.med_issue, 0) * 5) >= 60 then 'High'
      when least(100, coalesce(i.high, 0) * 20 + coalesce(i.med, 0) * 10 + coalesce(i.low, 0) * 5
               + coalesce(m.missed, 0) * 8 + coalesce(i.med_issue, 0) * 5) >= 30 then 'Medium'
      else 'Low'
    end,
    jsonb_build_object(
      'open_high_insights', coalesce(i.high, 0),
      'open_medium_insights', coalesce(i.med, 0),
      'open_low_insights', coalesce(i.low, 0),
      'missed_visits_14d', coalesce(m.missed, 0),
      'medication_issues', coalesce(i.med_issue, 0)
    ),
    now()
  from public.clients c
  left join (
    select client_id,
           count(*) filter (where severity = 'high') as high,
           count(*) filter (where severity = 'medium') as med,
           count(*) filter (where severity = 'low') as low,
           count(*) filter (where insight_type ilike '%medication%') as med_issue
    from public.care_insights
    where not resolved
    group by client_id
  ) i on i.client_id = c.id
  left join (
    -- assigned visit-days in the last 14 days with no check_in logged
    select v.client_id, count(*) as missed
    from generate_series(current_date - 13, current_date - 1, interval '1 day') as day
    join public.visits v on v.day_offset = extract(isodow from day)::int - 1
    join public.visit_assignments va on va.visit_id = v.id
    where not exists (
      select 1 from public.visit_logs vl
      where vl.visit_id = v.id and vl.carer_id = va.carer_id
        and vl.event_type = 'check_in' and vl.recorded_at::date = day::date
    )
    group by v.client_id
  ) m on m.client_id = c.id
  on conflict (client_id) do update
    set score = excluded.score, band = excluded.band,
        factors = excluded.factors, updated_at = excluded.updated_at;
end;
$$;

