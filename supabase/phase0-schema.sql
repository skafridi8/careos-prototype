-- Is the current user part of the care team (carer or manager)?
create or replace function public.is_care_team()
returns boolean
language sql
security definer set search_path = public
stable
as $$
  select exists (
    select 1 from public.profiles where id = auth.uid() and role in ('carer', 'manager')
  );
$$;

-- Which client is the current (family) user linked to? Null for everyone else.
create or replace function public.linked_client_id()
returns text
language sql
security definer set search_path = public
stable
as $$
  select client_id from public.family_members where profile_id = auth.uid() limit 1;
$$;

