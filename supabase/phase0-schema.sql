-- Seed ~90 days of history for the analytics trend charts (Feature Area 3).
-- Deterministic (setseed) so re-creation from scratch gives the same demo data:
-- ~93% of assigned visit-days get a check-in/out pair, ~82% of check-ins are on
-- time (within 15 min of schedule), coordinates jitter around the client's home.
do $$
declare
  day date;
  v record;
  late_min integer;
  cin timestamptz;
begin
  if exists (select 1 from public.visit_logs) then return; end if;
  perform setseed(0.42);

  for day in select generate_series(current_date - 90, current_date - 1, interval '1 day')::date loop
    for v in
      select vi.id as visit_id, va.carer_id, vi.scheduled_start, vi.scheduled_end,
             c.latitude, c.longitude
      from public.visits vi
      join public.visit_assignments va on va.visit_id = vi.id
      join public.clients c on c.id = vi.client_id
      where vi.day_offset = extract(isodow from day)::int - 1
        and vi.id not like 'v-open%'
    loop
      if random() < 0.93 then
        -- on time (0-14 min late) 82% of the time, otherwise 15-40 min late
        if random() < 0.82 then late_min := floor(random() * 15)::int;
        else late_min := 15 + floor(random() * 26)::int;
        end if;
        cin := day + v.scheduled_start + make_interval(mins => late_min);
        insert into public.visit_logs (visit_id, carer_id, event_type, recorded_at, latitude, longitude) values
          (v.visit_id, v.carer_id, 'check_in',  cin, v.latitude + (random() - 0.5) * 0.0008, v.longitude + (random() - 0.5) * 0.0008),
          (v.visit_id, v.carer_id, 'check_out', cin + (v.scheduled_end - v.scheduled_start) + make_interval(mins => floor(random() * 8)::int),
           v.latitude + (random() - 0.5) * 0.0008, v.longitude + (random() - 0.5) * 0.0008);
      end if;
    end loop;
  end loop;
end $$;

