-- ============================================================
-- Analytics views (Feature Area 3) — computed from real rows, not hardcoded.
-- ============================================================
-- Daily time series over visit_logs: completion, punctuality, medication logging.
-- security_invoker: the view runs with the querying user's RLS, so it cannot be
-- used to read rows the user's role is not allowed to see.
create or replace view public.care_quality_daily
with (security_invoker = true) as
with expected as (
  select day::date as day,
         count(*) as visits_expected,
         count(*) filter (where v.visit_type ilike '%medication%') as med_visits_expected
  from generate_series(current_date - 90, current_date, interval '1 day') as day
  join public.visits v on v.day_offset = extract(isodow from day)::int - 1
  join public.visit_assignments va on va.visit_id = v.id
  group by day::date
),
logged as (
  select vl.recorded_at::date as day,
         count(*) filter (where vl.event_type = 'check_in') as check_ins,
         count(*) filter (where vl.event_type = 'check_in'
                          and vl.recorded_at::time < v.scheduled_start + interval '15 minutes') as on_time_check_ins,
         count(*) filter (where vl.event_type = 'check_in'
                          and v.visit_type ilike '%medication%') as med_check_ins
  from public.visit_logs vl
  join public.visits v on v.id = vl.visit_id
  group by vl.recorded_at::date
)
select
  e.day,
  e.visits_expected,
  coalesce(l.check_ins, 0) as visits_completed,
  round(100.0 * coalesce(l.check_ins, 0) / nullif(e.visits_expected, 0), 1) as completion_pct,
  round(100.0 * coalesce(l.on_time_check_ins, 0) / nullif(l.check_ins, 0), 1) as on_time_pct,
  round(100.0 * coalesce(l.med_check_ins, 0) / nullif(e.med_visits_expected, 0), 1) as medication_log_pct
from expected e
left join logged l on l.day = e.day
order by e.day;

-- Single-row current summary: the "Care Quality Score" (0-100), weighted:
-- 35% visit completion + 25% punctuality + 20% medication logging
-- + 10% plan reviews in date + 10% absence of open high-severity insights.
create or replace view public.care_quality_summary
with (security_invoker = true) as
with last30 as (
  select avg(completion_pct) as completion_pct,
         avg(on_time_pct) as on_time_pct,
         avg(medication_log_pct) as medication_log_pct
  from public.care_quality_daily
  where day >= current_date - 30 and day < current_date
),
reviews as (
  select count(*) filter (where care_plan_review_due < current_date) as overdue,
         count(*) as total
  from public.clients
),
insights as (
  select count(*) filter (where severity = 'high') as open_high, count(*) as open_total
  from public.care_insights where not resolved
)
select
  round(l.completion_pct, 1) as completion_pct,
  round(l.on_time_pct, 1) as on_time_pct,
  round(l.medication_log_pct, 1) as medication_log_pct,
  r.overdue as overdue_plan_reviews,
  r.total as total_clients,
  i.open_high as open_high_insights,
  i.open_total as open_insights,
  round(
      0.35 * l.completion_pct
    + 0.25 * l.on_time_pct
    + 0.20 * l.medication_log_pct
    + 0.10 * (100.0 * (r.total - r.overdue) / nullif(r.total, 0))
    + 0.10 * greatest(0, 100 - i.open_high * 25)
  )::int as care_quality_score
from last30 l, reviews r, insights i;

-- Per-carer workload & punctuality over the last 30 days.
create or replace view public.carer_workload
with (security_invoker = true) as
select
  c.id as carer_id,
  c.name,
  count(*) filter (where vl.event_type = 'check_in') as visits_30d,
  round(100.0 * count(*) filter (where vl.event_type = 'check_in'
          and vl.recorded_at::time < v.scheduled_start + interval '15 minutes')
        / nullif(count(*) filter (where vl.event_type = 'check_in'), 0), 1) as on_time_pct,
  round(sum(
      case when vl.event_type = 'check_in'
      then extract(epoch from (v.scheduled_end - v.scheduled_start)) / 3600.0 else 0 end
    )::numeric, 1) as scheduled_hours_30d
from public.carers c
left join public.visit_logs vl on vl.carer_id = c.id and vl.recorded_at >= current_date - 30
left join public.visits v on v.id = vl.visit_id
group by c.id, c.name
order by visits_30d desc;

grant select on public.care_quality_daily, public.care_quality_summary, public.carer_workload to authenticated;

